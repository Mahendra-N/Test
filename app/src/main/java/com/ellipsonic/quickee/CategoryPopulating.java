public class CategoryPopulating {

    String Category;
    String term;


    public CategoryPopulating(String  Category, String  term) {
        this.Category = Category;
        this.term = term;
    }

    public String getCategory() {
        return this.Category;
    }

    public String getterm() {
        return this.term;
    }
}
