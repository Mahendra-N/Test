public class TermPopulating {

    String Term;
    String Desc;


    public TermPopulating(String  Term, String  Desc) {
        this.Term = Term;
        this.Desc = Desc;
    }

    public String getTerm() {
        return this.Term;
    }

    public String getDesc() {
        return this.Desc;
    }

}
